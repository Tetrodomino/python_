########## 파이썬의 변수 ##########
# 파이썬의 변수는 대소문자를 구분
# 따로 변수에 자료형 구분이 없어 정수형 값을 저장한 변수에 문자열이나 실수형 값을 넣어도 정상 작동

# 정수형 (int)
integ = 6

# 실수형 (float)
doub = 3.1233

# boolean
# True나 False의 첫 글자를 반드시 대문자로 작성
truefalse = True

# 문자열 (string)
# '' 이나 ""을 사용
strvar = "가나다라마바사"

print(integ)
print(doub)
print(truefalse)
print(strvar)

# 자료형 타입 출력하기
print(type(integ))
print(type(doub))
print(type(truefalse))
print(type(strvar))