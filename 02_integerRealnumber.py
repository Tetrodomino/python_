# 정수 자료형 (int)
10
10 + 10

# 실수 자료형 (float)
10.0
1.7
.5  # 정수부가 0이면 0 생략 가능

# 정수끼리의 계산 결과는 정수로 취급하지만, 실수가 하나라도 섞여들어가면 실수로 취급
1 + 2 + 3 + 4 + 5.0     # 5.0이 실수이므로 결과는 실수

# 나누기(/) 는 결과값이 정수가 나오더라도 항상 실수로 취급
10 / 2      # 결과는 정수지만 실제로 나오는 결과값은 5.0



# 부동소수점 실수
# 유효숫자 + e 지수 의 방식으로 표현
12300.0
123e2    # 둘은 같은 표현

123e-2   # 1.23

